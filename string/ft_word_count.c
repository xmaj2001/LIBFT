int	ft_word_count(const char *s, char d)
{
	int	len;
	int	i;

	i = 0;
	len = 0;
	while (s[i])
	{
		while (s[i] == d)
			i++;
		if (s[i] != '\0')
			len++;
		while (s[i] && s[i] != d)
			i++;
	}
	return (len);
}
